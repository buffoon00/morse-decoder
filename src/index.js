const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    let arrayMatch = [];
    for (i = 0; i < expr.length; i +=10) {
        arrayMatch.push(expr.substring(i, i + 10));
    }
    const arrayNew = arrayMatch.map((x) => x.replace(/00/g,'').replace(/10/g,'.').replace(/11/g,'-').replace(/\*{10}/g, ' '));
    const  arrayMorse = arrayNew.map((x) => x = MORSE_TABLE[x] || ' ').join('');
    return arrayMorse;
}

module.exports = {
    decode
}